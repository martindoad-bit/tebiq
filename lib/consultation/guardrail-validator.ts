export function validateAnswer(input: AnswerValidationInput): GuardrailFinding[] {
  const question = input.question ?? ''
  const answer = input.answer ?? ''
  const routeGateMatches = input.routeGateMatches ?? matchRouteGates(question)
  const findings = [
    ...validateAnswerCompleteness(answer, { routeGateMatches }),
    ...validatePermissionStateContradictions(question, answer),
  ]

  return dedupeFindings(findings)
}

export function selectTerminalGuardrailFindings(
  findings: GuardrailFinding[],
  options: { routeGateMatches?: RouteGateMatch[] } = {},
): GuardrailFinding[] {
  const hasRouteGate = (options.routeGateMatches ?? []).length > 0

  return findings.filter(finding => {
    if (finding.severity === 'P0') return true
    if (finding.id === 'answer-empty') return true
    if (finding.id === 'answer-open-list-marker') return true
    if (finding.id === 'answer-trailing-connector') return true
    if (finding.id === 'answer-too-short') return hasRouteGate
    if (finding.id === 'answer-no-terminal-punctuation') return finding.severity === 'P1'
    if (TERMINAL_CONTRADICTION_IDS.has(finding.id)) return true
    return false
  })
}

