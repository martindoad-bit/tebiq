export function matchRouteGates(input: string | {
  question?: string | null
  imageSummary?: string | null
} | null | undefined): RouteGateMatch[] {
  const raw = typeof input === 'string'
    ? input
    : [input?.question, input?.imageSummary].filter(Boolean).join('\n')
  const normalized = normalize(raw)
  if (!normalized) return []

  return ROUTE_GATE_PATTERNS
    .map(pattern => {
      const allMatched = pattern.match.allOf.every(group =>
        group.some(term => normalized.includes(normalize(term))),
      )
      if (!allMatched) return null

      const anyTerms = pattern.match.anyOf ?? []
      const matchedAny = anyTerms.filter(term => normalized.includes(normalize(term)))
      const minAny = pattern.match.minAny ?? (anyTerms.length > 0 ? 1 : 0)
      if (matchedAny.length < minAny) return null

      const matchedTerms = [
        ...pattern.match.allOf.flatMap(group =>
          group.filter(term => normalized.includes(normalize(term))),
        ),
        ...matchedAny,
      ]

      return { pattern, matchedTerms: Array.from(new Set(matchedTerms)) }
    })
    .filter((match): match is RouteGateMatch => Boolean(match))
}

export function routeGatesToPromptContext(matches: RouteGateMatch[]): string | null {
  if (matches.length === 0) return null

  const blocks = matches.map(({ pattern }, index) => [
    `【Route Gate ${index + 1}: ${pattern.title}】`,
    `severity: ${pattern.severity}`,
    `source_assets: ${pattern.sourceAssetIds.join(', ')}`,
    '',
    '回答前必须确认/抽取:',
    ...pattern.mustAskOrCheck.map(item => `- ${item}`),
    '',
    '必须保持的边界:',
    ...pattern.mustSay.map(item => `- ${item}`),
    '',
    '禁止说法:',
    ...pattern.mustNotSay.map(item => `- ${item}`),
  ].join('\n'))

  return [
    '以下是 TEBIQ 0.8 P0/P1 route gates。它们只用于防止危险捷径，不用于预测许可结果。',
    '回答要求:',
    '- 用简体中文回答，保留用户能执行的下一步。',
    '- 先区分状态、手续、材料、窗口和活动许可，不要把一个词直接当成结论。',
    '- 如果事实不足，先列出需要确认的原文/日期/窗口，再给保守行动顺序。',
    '- 不要承诺入管会许可、会受理、会补正、会保护到结果出来。',
    '',
    ...blocks,
  ].join('\n')
}

export function getRouteGateIds(matches: RouteGateMatch[]): string[] {
  return matches.map(match => match.pattern.id)
}

function normalize(value: string | null | undefined): string {
  return (value ?? '')
    .toLowerCase()
    .replace(/\s+/g, '')
    .replace(/[“”"']/g, '')
}
